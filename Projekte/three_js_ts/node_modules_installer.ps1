echo Initialisieren_des_Three.JS_Projektes...
$module_file_name = "node_modules"
$other_files=Get-ChildItem -File
$other_folders = Get-ChildItem -Exclude $other_files -Name

$test = Get-ChildItem -Name $module_file_name


function get_modules{
    foreach($other_folder_name in $other_folders){
        if ($other_folder_name -eq $module_file_name){
            return
        }
    }
    echo installieren_von_modulen
    npm install three
    npm install @types/three
    npm install webpack-cli webpack-dev-server webpack-merge ts-loader
    npm install typescript
}

get_modules
echo done!!
[Diagnostics.Process]::Start("cmd.exe",".\run.bat")